LABELLER_DISPLAY = {"gpt": "gpt-5.2", "qwen": "qwen3.6-plus", "kimi": "kimi-k2.6"}


def read_metrics(path: Path):
    d = json.loads(path.read_text()).get("test", {}) or {}
    return d.get("precision"), d.get("recall"), d.get("f1"), d.get("accuracy")


def find_predictions(run_dir: Path) -> str:
    candidates = list(run_dir.glob("**/predictions.csv"))
    return str(candidates[0]) if candidates else ""


def main():
    rows: List[Dict] = []
    for spec in PIPELINES:
        root = ROOT / spec["root"]
        if not root.exists():
            continue
        for mp in root.glob("**/metrics.json"):
            if "training_output" in mp.parts: continue
            # the run dir is mp.parent.parent (skip <bm>/metrics.json)
            bm_dir = mp.parent  # contains metrics.json
            run_dir = bm_dir.parent
            m = spec["re"].match(run_dir.name)
            if not m:
                continue
            gd = m.groupdict()
            # bm: prefer regex group, then spec fixed_bm, then the directory name containing metrics.json
            bm = gd.get("bm") or spec.get("fixed_bm") or bm_dir.name
            labeller = gd.get("labeller")
            if labeller is None:
                labeller = spec.get("labeller", "")
            else:
                labeller = LABELLER_DISPLAY.get(labeller, labeller)
            method = spec.get("method") or f"method_{gd.get('method_key','')}"
            if "method_key" in gd and "method" not in spec:
                method = {"seed_round": "similarity_selection", "simple_active": "simple_active_learning"}[gd["method_key"]]
            profile = gd.get("profile")
            if profile is None:
                if "fixed_profile" in spec:
                    profile = spec["fixed_profile"]
                elif "profile_per_bm" in spec:
                    profile = spec["profile_per_bm"].get(bm, "")
                elif "profile" in spec and callable(spec["profile"]):
                    profile = spec["profile"](bm)
                else:
                    profile = ""
            seed = int(gd["seed"]) if "seed" in gd else None
            repeat = int(gd["rep"]) if "rep" in gd else None
            prec, rec, f1, acc = read_metrics(mp)
            cleaned_root = spec.get("cleaned_root", "")
            cleaned_train = ""
            if cleaned_root and profile and bm:
                cand = ROOT / cleaned_root / labeller_key_for_method(spec, gd, labeller) / bm / "profiles" / profile / f"active_labels_latest_{bm}_{profile}_train.json.gz"
                # Fallbacks: some cleaned roots don't have labeller subdir
                if not cand.exists():
                    cand_alt = ROOT / cleaned_root / bm / "profiles" / profile / f"active_labels_latest_{bm}_{profile}_train.json.gz"
                    if cand_alt.exists():
                        cand = cand_alt
                cleaned_train = str(cand) if cand.exists() else ""
            rows.append({
                "pipeline": spec["pipeline"],
                "method": method,
                "labeller": labeller,
                "benchmark": bm,
                "profile": profile,
                "seed": seed,
                "repeat": repeat,
                "run_dir": str(run_dir.relative_to(ROOT)),
                "metrics_path": str(mp.relative_to(ROOT)),
                "predictions_path": find_predictions(bm_dir),
                "cleaned_train_path": cleaned_train,
                "precision": prec, "recall": rec, "f1": f1, "accuracy": acc,
                "comment": PIPELINE_COMMENTS.get(spec["pipeline"], ""),
            })

    cols = ["pipeline", "method", "labeller", "benchmark", "profile", "seed", "repeat",
            "run_dir", "metrics_path", "predictions_path", "cleaned_train_path",
            "precision", "recall", "f1", "accuracy", "comment"]
    OUT.parent.mkdir(parents=True, exist_ok=True)
    with OUT.open("w", newline="") as f:
        w = csv.DictWriter(f, fieldnames=cols)
        w.writeheader()
        for r in rows:
            w.writerow({k: r.get(k, "") for k in cols})
    print(f"Wrote {OUT} ({len(rows)} rows)")


def labeller_key_for_method(spec, gd, labeller_display):
    # Map labeller back to short key for path lookup.
    if spec.get("pipeline","").startswith("size_scan_abtbuy") and "labeller" in gd:
        return gd["labeller"]
    if spec.get("method") == "ditto_active_learning":
        return {"gpt-5.2": "gpt", "qwen3.6-plus": "qwen", "kimi-k2.6": "kimi"}.get(labeller_display, "")
    return ""


if __name__ == "__main__":
    main()
